    private void btnAddRoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddRoadActionPerformed
        // TODO add your handling code here:
        String cmb1 = jComboBox1.getSelectedItem().toString();
        String cmb2 = jComboBox2.getSelectedItem().toString();
        if (cmb1!=null && cmb2!=null && cmb1.equals(cmb2)==false){
            boolean paso;
            if (jRadioButton1.isSelected()==true){
                paso = true;
            }else{
                paso = false;
            }
            int distancia;
            int peso;
            int velmax;
            try{
                distancia = Integer.parseInt(jTextField1.getText());
                peso = Integer.parseInt(jTextField2.getText());
                velmax = Integer.parseInt(jTextField3.getText());
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, "Favor digitar un n√∫mero v√°lido!");
                return;
            }
            if (met.insertarArco(met.buscar(cmb1), met.buscar(cmb2), distancia, paso, velmax, peso)){
                JOptionPane.showMessageDialog(null, "Camino agregado");
                return;
            }
            JOptionPane.showMessageDialog(null, "Camino NO agregado");
            return;
        }
        JOptionPane.showMessageDialog(null, "Favor seleccionar un camino v√°lido!");
    }//GEN-LAST:event_btnAddRoadActionPerformed

