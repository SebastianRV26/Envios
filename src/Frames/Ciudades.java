    private void addCityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCityActionPerformed
        // método que agrega una ciudad
        String name = JOptionPane.showInputDialog("Nombre de la ciudad");
        if (met.insertarVertices(name)) {
            JOptionPane.showMessageDialog(null, "Insertado");
            return;
        }
        JOptionPane.showMessageDialog(null, "No insertado");
    }//GEN-LAST:event_addCityActionPerformed

    private void editCityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editCityActionPerformed
        // método que elimina una ciudad
        String name = JOptionPane.showInputDialog("Editar el nombre de la ciudad");
        if (jComboBox1.getSelectedItem() != null) {
            if (name != null) {
                if (met.modificarVertice(met.buscar(jComboBox1.getSelectedItem().toString()), name)) {
                    JOptionPane.showMessageDialog(null, "Editado!");
                } else {
                    JOptionPane.showMessageDialog(null, "No editado!");
                }
                llenarCombobox();
            }
        }

    }//GEN-LAST:event_editCityActionPerformed

    private void deleteCityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteCityActionPerformed
        // TODO add your handling code here:
        if (jComboBox1.getSelectedItem()!=null) {
            Ciudad city = met.buscar(jComboBox1.getSelectedItem().toString());
            if (city != null) {
                if (met.eliminarVertice(city)) {
                    JOptionPane.showMessageDialog(null, "Eliminado!");
                } else {
                    JOptionPane.showMessageDialog(null, "No eliminado!");
                }
                llenarCombobox();
                return;
            }
        }
        JOptionPane.showMessageDialog(null, "No encontrado!");

    }//GEN-LAST:event_deleteCityActionPerformed

