    public void llenarCombobox() {
        //metodo que recorre la lista de vértices y los agrega a un combobox
        jComboBox1.removeAllItems();
        Ciudad aux = met.grafo;
        while (aux != null) {
            jComboBox1.addItem(aux.nombre);
            aux = aux.sigV;
        }
    }
